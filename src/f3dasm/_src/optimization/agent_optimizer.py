    # Message composers
    # ------------------------------------------------------------------

    def _compose_turn_message(self, turn_id: int) -> str:
        """Render the context block for a Strategizer turn.

        Parameters
        ----------
        turn_id : int
            Current turn index.

        Returns
        -------
        str
            The turn message string.
        """

        context = self._render_context()
        if turn_id == 0:
            preamble = (
                f"Turn {turn_id}. No evaluations have happened yet. "
                "State of the world below."
            )
        else:
            preamble = f"Turn {turn_id}. State of the world below."
        return f"{preamble}\n\n{context}"

    def _compose_implementer_message(
        self, turn_id: int, strategy: Strategy
    ) -> str:
        """Render the Implementer's turn message including the Strategy.

        Parameters
        ----------
        turn_id : int
            Current turn index.
        strategy : Strategy
            The Strategy payload emitted by the Strategizer.

        Returns
        -------
        str
            The turn message string.
        """

        context = self._render_context()
        strategy_block = (
            "## Strategy from Strategizer\n"
            f"Name: {strategy.name}\n"
            f"Steps: {strategy.n_steps}\n"
            f"Params: {json.dumps(strategy.params)}\n\n"
            f"### Intent\n{strategy.intent}"
        )
        preamble = f"Turn {turn_id}. Execute the following strategy."
        return f"{preamble}\n\n{context}\n\n{strategy_block}"

    # ------------------------------------------------------------------
    # Schema rendering helpers
    # ------------------------------------------------------------------

    @staticmethod
    def _render_variable_parameters(schema: ProblemSchema) -> str:
        """Render variable parameters as a compact text block.

        Parameters
        ----------
        schema : ProblemSchema
            The problem description.

        Returns
        -------
        str
            Multi-line text.
        """

        if not schema.variable_parameters:
            return "(none)"
        lines: list[str] = []
        for name, spec in schema.variable_parameters.items():
            if spec.kind == "categorical" and spec.categories:
                lines.append(
                    f"  {name} (categorical): {spec.categories}"
                )
            elif spec.bounds is not None:
                lo, hi = spec.bounds
                scale = " [log-scale]" if spec.log_scale else ""
                lines.append(
                    f"  {name} ({spec.kind}): [{lo:g}, {hi:g}]{scale}"
                )
            else:
                lines.append(f"  {name} ({spec.kind})")
        return "\n".join(lines)

    @staticmethod
    def _render_output_columns(schema: ProblemSchema) -> str:
        """Render output columns as a compact text block.

        Parameters
        ----------
        schema : ProblemSchema
            The problem description.

        Returns
        -------
        str
            Multi-line text.
        """

        if not schema.output_columns:
            return "(none)"
        lines: list[str] = []
        for name, spec in schema.output_columns.items():
            desc = f" — {spec.description}" if spec.description else ""
            if spec.kind == "categorical" and spec.categories:
                lines.append(
                    f"  {name} (categorical, {spec.categories}){desc}"
                )
            else:
                lines.append(f"  {name} (continuous){desc}")
        return "\n".join(lines)


