    # Internal helpers
    # ------------------------------------------------------------------

    def _build_problem_schema(
        self, data: ExperimentData, output_name: str
    ) -> ProblemSchema:
        """Derive a ``ProblemSchema`` from the live domain and config.

        Parameters
        ----------
        data : ExperimentData
            The shared dataset (read for domain info).
        output_name : str
            Name of the scalar objective output column.

        Returns
        -------
        ProblemSchema
            Immutable problem description.
        """

        domain = data.domain

        # Collect forbidden names: constant parameters + user extras.
        from ..design.parameter import ConstantParameter
        forbidden: list[str] = [
            name
            for name, param in domain.input_space.items()
            if isinstance(param, ConstantParameter)
        ]
        forbidden.extend(self._forbidden_extra)
        forbidden_set = set(forbidden)

        # Build variable_parameters for non-forbidden, non-constant inputs.
        variable_parameters: dict[str, ParameterSpec] = {}
        from ..design.parameter import (
            CategoricalParameter,
            ContinuousParameter,
            DiscreteParameter,
        )
        for name, param in domain.input_space.items():
            if name in forbidden_set:
                continue
            if isinstance(param, ContinuousParameter):
                variable_parameters[name] = ParameterSpec(
                    kind="continuous",
                    bounds=(param.lower_bound, param.upper_bound),
                    categories=None,
                    log_scale=False,
                )
            elif isinstance(param, DiscreteParameter):
                variable_parameters[name] = ParameterSpec(
                    kind="discrete",
                    bounds=(
                        float(param.lower_bound),
                        float(param.upper_bound),
                    ),
                    categories=None,
                    log_scale=False,
                )
            elif isinstance(param, CategoricalParameter):
                variable_parameters[name] = ParameterSpec(
                    kind="categorical",
                    bounds=None,
                    categories=list(param.categories),
                    log_scale=False,
                )
            # ConstantParameter is already excluded above.

        return ProblemSchema(
            variable_parameters=variable_parameters,
            forbidden_parameters=list(forbidden),
            objective_name=output_name,
            objective_direction=self._objective_direction,
            output_columns=dict(self._output_columns),
            physics_context=self._physics_context,
        )

    @staticmethod
    def _extract_rationale(response: str) -> tuple[str, str]:
        """Extract the ``## Rationale`` section from an agent response.

        Parameters
        ----------
        response : str
            The final assistant message text.

        Returns
        -------
        tuple of (str, str)
            ``(rationale_text, result)`` where ``result`` is ``'ok'`` on
            success or ``'error'`` if no ``## Rationale`` section exists.
        """

        marker = "## Rationale"
        idx = response.find(marker)
        if idx == -1:
            return "", "error"
        # Everything after the marker heading line.
        after = response[idx + len(marker):]
        # Trim leading newline.
        rationale = after.lstrip("\n").strip()
        # Stop at the next heading.
        next_heading = rationale.find("\n##")
        if next_heading != -1:
            rationale = rationale[:next_heading].strip()
        return rationale, "ok"

    def _persist_turn_log(self, data: ExperimentData) -> None:
        """Write the full turn log to ``<project_dir>/turn_log.jsonl``.

        Parameters
        ----------
        data : ExperimentData
            Used to locate the project directory.
        """

        project_dir: Path = data._project_dir
        log_path = project_dir / "turn_log.jsonl"
        lines = [record.to_json() for record in self.turn_log]
        log_path.write_text("\n".join(lines) + "\n")

    def _consecutive_errors(self) -> int:
        """Count the number of trailing ``result='error'`` entries in the log.

        Returns
        -------
        int
            Length of the trailing error run.
        """

        count = 0
        for record in reversed(self.turn_log):
            if record.result == "error":
                count += 1
            else:
                break
        return count


