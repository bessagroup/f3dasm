def _build_replicate_script(
    calls_repr: str,
    objective_name: str,
    direction: str,
) -> str:
    """Return the source of the auto-generated ``replicate.py`` script.

    Building the script at module level avoids the ``textwrap.dedent``
    pitfall: when multi-line f-string interpolations (e.g. the
    JSON-serialised strategy-calls list) introduce lines with less
    leading whitespace than the surrounding template, ``dedent`` has
    no common prefix to strip and leaves all indentation intact.

    Parameters
    ----------
    calls_repr : str
        JSON-serialised list of ``(name, n_steps, params)`` triples.
    objective_name : str
        Name of the scalar objective output column.
    direction : str
        ``'maximize'`` or ``'minimize'``.

    Returns
    -------
    str
        Full Python source code for ``replicate.py``.
    """

    lines: list[str] = [
        '"""Replicate the agentic run without invoking any LLM.',
        "",
        "This script was auto-generated by",
        "``AgentOptimizer.write_deliverable()``.",
        "",
        "It loads the final ExperimentData snapshot, then replays every",
        "strategy_calls triple recorded in turn_log.jsonl using the",
        "strategy adapters from f3dasm.agentic.  No LLM is called.",
        "The best design is printed to stdout.",
        "",
        "Usage",
        "-----",
        "python replicate.py",
        '"""',
        "from __future__ import annotations",
        "",
        "import sys",
        "from pathlib import Path",
        "",
        "# Locate the deliverable directory (same folder as this script).",
        "_DELIV_DIR = Path(__file__).resolve().parent",
        "",
        "# Insert the f3dasm src path if running from the repo.",
        "_REPO_ROOT = _DELIV_DIR",
        "for _ in range(6):",
        "    candidate = _REPO_ROOT / 'src'",
        "    if (candidate / 'f3dasm').exists():",
        "        sys.path.insert(0, str(candidate))",
        "        break",
        "    _REPO_ROOT = _REPO_ROOT.parent",
        "",
        "from f3dasm import ExperimentData  # noqa: E402",
        "from f3dasm.agentic import default_registry  # noqa: E402",
        "",
        "# Strategy calls recorded during the original run, in order.",
        f"_STRATEGY_CALLS = {calls_repr}",
        "",
        "",
        "def _best_design(",
        "    data: ExperimentData,",
        "    objective_name: str,",
        "    direction: str,",
        ") -> dict:",
        '    """Return the best design from the dataset."""',
        "    best_val = None",
        "    best_row: dict = {}",
        "    for idx, sample in data.data.items():",
        "        obj = sample._output_data.get(objective_name)",
        "        if obj is None:",
        "            continue",
        "        try:",
        "            obj_f = float(obj)",
        "        except (TypeError, ValueError):",
        "            continue",
        "        if obj_f != obj_f:",
        "            continue",
        "        if best_val is None:",
        "            improved = True",
        '        elif direction == "maximize" and obj_f > best_val:',
        "            improved = True",
        '        elif direction == "minimize" and obj_f < best_val:',
        "            improved = True",
        "        else:",
        "            improved = False",
        "        if improved:",
        "            best_val = obj_f",
        "            best_row = {",
        '                "experiment_id": idx,',
        '                "inputs": dict(sample._input_data),',
        '                "outputs": dict(sample._output_data),',
        "            }",
        "    return best_row",
        "",
        "",
        "def main() -> None:",
        '    """Load the snapshot and print the best design."""',
        "",
        "    data_dir = _DELIV_DIR / 'experiment_data'",
        "    data = ExperimentData.from_file(data_dir)",
        "",
        "    # Replay strategy calls (no LLM).",
        "    adapters, _ = default_registry()",
        "    for name, n_steps, params in _STRATEGY_CALLS:",
        "        if name not in adapters:",
        "            print(",
        "                f'WARNING: strategy {name!r} not found in '",
        "                'default_registry; skipping.'",
        "            )",
        "            continue",
        "        # Adapters: adapter(data, n_steps, params).",
        "        try:",
        "            adapters[name](data, n_steps, params)",
        "        except Exception as exc:",
        "            print(",
        "                f'WARNING: strategy {name!r} raised '",
        "                f'{type(exc).__name__}: {exc}'",
        "            )",
        "",
        "    best = _best_design(",
        f'        data, "{objective_name}", "{direction}"',
        "    )",
        "    if not best:",
        "        print('No best design found.')",
        "        return",
        "",
        "    print('Best design:')",
        "    print(f\"  experiment_id : {best['experiment_id']}\")",
        '    for col, val in best["inputs"].items():',
        "        print(f'  {col:25s}: {val}')",
        '    for col, val in best["outputs"].items():',
        "        print(f'  {col:25s}: {val}')",
        "",
        "",
        'if __name__ == "__main__":',
        "    main()",
        "",
    ]
    return "\n".join(lines)


