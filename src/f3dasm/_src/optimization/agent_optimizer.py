class AgentOptimizer(Optimizer):
    """Abstract base for agentic optimizers in the agentic-f3dasm layer.

    Subclasses implement ``call()`` to define the inter-agent topology
    (e.g. Strategizer → Implementer).  The base class owns the turn-loop
    scaffolding: building tools, dispatching agent sessions, extracting
    rationales, recording ``TurnRecord``s, and updating the
    ``AnalysisBase`` artifacts.

    Parameters
    ----------
    physics_context : str
        Day-1 problem briefing shown to agents in their system prompts.
    output_columns : dict[str, OutputSpec]
        Typed metadata for every output column.  Supplied explicitly
        because ``Domain`` does not carry kind/categories metadata.
    forbidden_extra : list of str, optional
        Additional parameter names to add to the schema's forbidden list
        beyond the constant parameters auto-detected from ``Domain``.
    objective_direction : {'minimize', 'maximize'}, default 'maximize'
        Direction of optimization for the objective column.
    """

    def __init__(
        self,
        physics_context: str,
        output_columns: dict[str, OutputSpec],
        forbidden_extra: list[str] = (),
        objective_direction: ObjectiveDirection = "maximize",
    ) -> None:
        """Initialise the base optimizer with problem context.

        Parameters
        ----------
        physics_context : str
            Day-1 problem briefing shown to agents.
        output_columns : dict[str, OutputSpec]
            Typed metadata for every output column.
        forbidden_extra : list of str, optional
            Extra forbidden parameter names.
        objective_direction : {'minimize', 'maximize'}, default 'maximize'
            Direction of optimization.
        """

        self._physics_context = physics_context
        self._output_columns = dict(output_columns)
        self._forbidden_extra = list(forbidden_extra)
        self._objective_direction: ObjectiveDirection = objective_direction

        # These are populated by arm().
        self.turn_log: list[TurnRecord] = []
        self.analysis_base: dict[str, str] = {}
        self.agents: dict[str, Agent] = {}

    # ------------------------------------------------------------------
    # arm
    # ------------------------------------------------------------------

    def arm(
        self,
        data: ExperimentData,
        data_generator: DataGenerator,
        input_name: str,
        output_name: str,
    ) -> None:
        """Prepare the optimizer for a run.

        Registers the ``__turn`` output column on the domain, stamps
        pre-existing rows with ``SEED_TURN``, builds ``ProblemSchema`` and
        the strategy registry, and initialises ``AnalysisBase``.

        Parameters
        ----------
        data : ExperimentData
            The shared dataset.  Modified in place to register ``__turn``.
        data_generator : DataGenerator
            The evaluator that will be called by strategy adapters.
        input_name : str
            Name of the input parameter group (forwarded to subclasses).
        output_name : str
            Name of the scalar objective output column.
        """

        self.data_generator = data_generator
        self.input_name = input_name
        self.output_name = output_name
        self.data_domain = data.domain

        # Register __turn as an output column so f3dasm's schema-bound
        # ExperimentData can accept it on every new row.
        data.domain.add_output("__turn", to_disk=False, exist_ok=True)

        # Stamp all pre-existing (seed) rows with SEED_TURN so narrate()
        # can skip them cleanly.
        for sample in data.data.values():
            sample._output_data["__turn"] = SEED_TURN

        # Build the ProblemSchema from the live Domain.
        self.problem_schema = self._build_problem_schema(
            data=data,
            output_name=output_name,
        )

        # Build the strategy registry (adapters + specs).
        self.strategy_adapters, self.strategy_specs = default_registry()

        # Initialise AnalysisBase by running the three producers on the
        # seed data and the (empty) turn log.
        self.analysis_base = {
            "objective_summary": objective_summary(
                data, self.problem_schema, self.turn_log
            ),
            "coverage_summary": coverage_summary(
                data, self.problem_schema, self.turn_log
            ),
            "last_5_rationales": last_5_rationales(
                data, self.problem_schema, self.turn_log
            ),
        }

