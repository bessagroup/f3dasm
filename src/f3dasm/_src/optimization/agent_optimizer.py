    # ------------------------------------------------------------------
    # _execute_turn
    # ------------------------------------------------------------------

    def _execute_turn(
        self,
        turn_id: int,
        agent_name: str,
        message: str,
        data: ExperimentData,
    ) -> ExperimentData:
        """Execute a single agent turn and record its provenance.

        Parameters
        ----------
        turn_id : int
            Monotonically increasing turn counter.
        agent_name : str
            Key into ``self.agents``.
        message : str
            The orchestrator's turn message sent to the agent.
        data : ExperimentData
            The shared dataset (mutated in place by ``run_strategy``).

        Returns
        -------
        ExperimentData
            The dataset (same object, mutated in place).
        """

        timestamp = datetime.now(timezone.utc).isoformat()

        # Build per-turn mutable accumulators.
        strategy_calls: list[tuple[str, int, dict]] = []
        emit_slot: list[Strategy] = []

        # Build the common read-only tools for all agents.
        read_tools = [
            make_get_problem_schema(self.problem_schema),
            make_list_strategies(self.strategy_specs),
            make_summarize_experiment_data(data, self.problem_schema),
            make_read_artifact(self.analysis_base),
        ]

        # Build role-specific write tools.
        if agent_name == "strategizer":
            role_tools = [
                make_emit_strategy(self.strategy_specs, emit_slot),
            ]
        else:
            # Implementer role: run_strategy + ask_strategizer.
            followup_budget: list[int] = [self.max_followups]
            role_tools = [
                make_run_strategy(
                    adapters=self.strategy_adapters,
                    specs=self.strategy_specs,
                    data=data,
                    domain=self.data_domain,
                    data_generator=self.data_generator,
                    objective_name=self.output_name,
                    turn_id=turn_id,
                    strategy_calls_accumulator=strategy_calls,
                ),
                make_ask_strategizer(
                    dispatch_fn=self._followup_dispatch,
                    budget=followup_budget,
                ),
            ]

        tools = read_tools + role_tools

        # Dispatch the agent session.
        response = self.agents[agent_name].send(message, tools=tools)

        # Extract the rationale from the ## Rationale markdown section.
        rationale, result = self._extract_rationale(response)

        # Retrieve followup question if the agent called ask_strategizer.
        followup_question: Optional[str] = None
        if agent_name != "strategizer":
            # Scan for evidence of a followup in the strategy_calls;
            # the question itself is not recoverable from the closure here
            # so leave it None (Level-2 concern: transcript parsing).
            pass

        # Determine emitted strategy for Strategizer records.
        emitted_strategy: Optional[Strategy] = None
        if agent_name == "strategizer" and emit_slot:
            emitted_strategy = emit_slot[0]

        # Diff: query rows added during this turn by checking __turn stamp.
        experiment_ids = [
            idx
            for idx, sample in data.data.items()
            if sample._output_data.get("__turn") == turn_id
        ]

        # Append the TurnRecord to the log.
        record = TurnRecord(
            turn_id=turn_id,
            timestamp=timestamp,
            agent_name=agent_name,
            rationale=rationale,
            strategy_calls=list(strategy_calls),
            emitted_strategy=emitted_strategy,
            followup_question=followup_question,
            experiment_ids=experiment_ids,
            result=result,
            transcript_ref=None,
        )
        self.turn_log.append(record)

        # Persist the full turn log to disk.
        self._persist_turn_log(data)

        # Update the AnalysisBase artifacts.
        self.analysis_base["objective_summary"] = objective_summary(
            data, self.problem_schema, self.turn_log
        )
        self.analysis_base["coverage_summary"] = coverage_summary(
            data, self.problem_schema, self.turn_log
        )
        self.analysis_base["last_5_rationales"] = last_5_rationales(
            data, self.problem_schema, self.turn_log
        )

        # Check for three consecutive error turns.
        if self._consecutive_errors() >= 3:
            raise AgentExecutionError(
                "AgentOptimizer aborted: three consecutive turns returned "
                "result='error'. Check agent configuration and prompts."
            )

        return data

    # ------------------------------------------------------------------
    # _early_stop
    # ------------------------------------------------------------------

    def _early_stop(
        self, data: ExperimentData, turn_log: list[TurnRecord]
    ) -> bool:
        """Hook for subclasses to implement early termination.

        Default: always return ``False`` (no early stopping).

        Parameters
        ----------
        data : ExperimentData
            The shared dataset.
        turn_log : list of TurnRecord
            The current turn log.

        Returns
        -------
        bool
            ``True`` to stop the loop early; ``False`` to continue.
        """

        return False

    # ------------------------------------------------------------------
    # narrate
    # ------------------------------------------------------------------

    def narrate(self) -> str:
        """Produce a prose account of objective improvements across turns.

        Walks ``experiment_ids`` in insertion order, tracking the running
        best objective.  For each row that improved the best-so-far, looks
        up the corresponding ``TurnRecord.rationale`` and renders a short
        paragraph.  Skips rows with ``__turn < 0`` (seed rows).

        Returns
        -------
        str
            Multi-paragraph prose.  Empty string if no improvements were
            recorded.
        """

        if not hasattr(self, "problem_schema"):
            return "arm() has not been called yet; no history to narrate."

        direction = self.problem_schema.objective_direction
        objective_name = self.problem_schema.objective_name

        # Build a mapping from turn_id to rationale.
        rationale_by_turn: dict[int, str] = {
            rec.turn_id: rec.rationale for rec in self.turn_log
        }

        # Walk all (data) rows in insertion order.
        # We need the data reference — stored by subclasses during call().
        if not hasattr(self, "_last_data") or self._last_data is None:
            return "No data available yet."

        data: ExperimentData = self._last_data
        best_val: Optional[float] = None
        paragraphs: list[str] = []

        for idx, sample in data.data.items():
            turn_stamp = sample._output_data.get("__turn")
            # Skip seed rows.
            if turn_stamp is None or int(turn_stamp) < 0:
                continue

            obj = sample._output_data.get(objective_name)
            if obj is None:
                continue
            try:
                obj_float = float(obj)
            except (TypeError, ValueError):
                continue
            if obj_float != obj_float:
                continue

            # Check improvement.
            improved = False
            if best_val is None:
                improved = True
            elif direction == "maximize" and obj_float > best_val:
                improved = True
            elif direction == "minimize" and obj_float < best_val:
                improved = True

            if improved:
                best_val = obj_float
                rationale = rationale_by_turn.get(int(turn_stamp), "")
                paragraphs.append(
                    f"experiment_id={idx}: objective improved to "
                    f"{obj_float:.6g} (turn {turn_stamp})."
                    + (f"\n  Rationale: {rationale}" if rationale else "")
                )

        return "\n\n".join(paragraphs) if paragraphs else ""

    # ------------------------------------------------------------------
    # write_deliverable
    # ------------------------------------------------------------------

    def write_deliverable(
        self,
        out_dir: Path | str,
        *,
        baseline_value: Optional[float] = None,
        ceiling_value: Optional[float] = None,
    ) -> Path:
        """Write a self-contained run deliverable folder.

        The folder mirrors what a graduate student would hand in at the
        end of a study: the best design found, the reasoning narrative,
        a reproducibility script, the full provenance log, and a
        snapshot of the final ``ExperimentData``.

        Parameters
        ----------
        out_dir : Path or str
            Destination folder.  Created if it does not exist.
        baseline_value : float, optional
            Competing approach's objective result.  When provided, the
            ``## Comparison`` section states whether the agent beat it.
        ceiling_value : float, optional
            Theoretical maximum reachable in the available data (e.g.
            the pool ceiling).  When provided, the ``## Comparison``
            section shows ``agent_best / ceiling_value`` as a percentage.

        Returns
        -------
        Path
            Absolute path to ``out_dir``.

        Raises
        ------
        RuntimeError
            If ``_last_data`` is ``None``, meaning ``call()`` has not
            been invoked yet.
        """

        if not hasattr(self, "_last_data") or self._last_data is None:
            raise RuntimeError(
                "write_deliverable() must be called after call()."
            )

        out_dir = Path(out_dir).resolve()
        out_dir.mkdir(parents=True, exist_ok=True)

        data = self._last_data

        # ----------------------------------------------------------
        # Locate best design from the live dataset.
        # ----------------------------------------------------------
        objective_name = self.problem_schema.objective_name
        direction = self.problem_schema.objective_direction

        best_idx: Optional[int] = None
        best_val: Optional[float] = None
        for idx, sample in data.data.items():
            obj = sample._output_data.get(objective_name)
            if obj is None:
                continue
            try:
                obj_f = float(obj)
            except (TypeError, ValueError):
                continue
            # Skip NaN.
            if obj_f != obj_f:
                continue
            if best_val is None:
                improved = True
            elif direction == "maximize" and obj_f > best_val:
                improved = True
            elif direction == "minimize" and obj_f < best_val:
                improved = True
            else:
                improved = False
            if improved:
                best_val = obj_f
                best_idx = idx

        # ----------------------------------------------------------
        # Build solution.md
        # ----------------------------------------------------------
        solution_lines: list[str] = ["# Solution\n"]

        if best_idx is None or best_val is None:
            solution_lines.append(
                "No rows were produced during this run; "
                "no best design is available.\n"
            )
        else:
            best_sample = data.data[best_idx]
            solution_lines.append(
                f"Best design found (experiment_id={best_idx}):\n"
            )
            # Input parameter values.
            for col, val in best_sample._input_data.items():
                try:
                    solution_lines.append(f"- **{col}**: {float(val):.6g}")
                except (TypeError, ValueError):
                    solution_lines.append(f"- **{col}**: {val}")
            # Objective value.
            solution_lines.append(
                f"\n**{objective_name}** ({direction}): "
                f"{best_val:.6g}\n"
            )

        # ## Comparison section
        comparison_lines: list[str] = []
        if ceiling_value is not None or baseline_value is not None:
            comparison_lines.append("## Comparison\n")
            if (
                ceiling_value is not None
                and best_val is not None
                and ceiling_value != 0.0
            ):
                pct = 100.0 * best_val / ceiling_value
                comparison_lines.append(
                    f"- Agent best vs. pool ceiling: "
                    f"{best_val:.6g} / {ceiling_value:.6g} "
                    f"= **{pct:.1f}%**"
                )
            if baseline_value is not None and best_val is not None:
                if direction == "maximize":
                    beat = best_val > baseline_value
                else:
                    beat = best_val < baseline_value
                verdict = "beat" if beat else "did not beat"
                comparison_lines.append(
                    f"- Agent {verdict} the baseline "
                    f"({baseline_value:.6g})."
                )
            comparison_lines.append("")

        # ## How the solution was reached
        narration = self.narrate()
        narration_section = (
            "## How the solution was reached\n\n"
            + (narration if narration else "(no objective improvements "
               "were recorded during this run)")
            + "\n"
        )

        # ## Run metadata
        n_turns = len(self.turn_log)
        # Count seed rows (those with __turn == SEED_TURN, i.e. < 0).
        n_seed = sum(
            1
            for sample in data.data.values()
            if int(sample._output_data.get("__turn", 0)) < 0
        )
        n_evaluated = len(data.data) - n_seed
        n_agents = len(self.agents)
        agent_models: list[str] = []
        for agent_name_key, agent_obj in self.agents.items():
            model_id = getattr(agent_obj, "model", None)
            if model_id is None:
                model_id = getattr(agent_obj, "_model", "<unknown>")
            agent_models.append(f"{agent_name_key}: {model_id}")

        metadata_lines: list[str] = [
            "## Run metadata\n",
            f"- Turns recorded: {n_turns}",
            f"- Evaluations (non-seed rows): {n_evaluated}",
            f"- Agents involved: {n_agents}",
        ]
        for model_line in agent_models:
            metadata_lines.append(f"  - {model_line}")
        metadata_lines.append("")

        # Assemble and write solution.md.
        full_solution = (
            "\n".join(solution_lines)
            + "\n"
            + ("\n".join(comparison_lines) + "\n" if comparison_lines else "")
            + narration_section
            + "\n"
            + "\n".join(metadata_lines)
        )
        (out_dir / "solution.md").write_text(full_solution, encoding="utf-8")

        # ----------------------------------------------------------
        # Copy turn_log.jsonl
        # ----------------------------------------------------------
        src_log = data._project_dir / "turn_log.jsonl"
        dest_log = out_dir / "turn_log.jsonl"
        if src_log.exists():
            shutil.copy2(src_log, dest_log)
        else:
            # Write from in-memory log if the file was never flushed.
            lines = [rec.to_json() for rec in self.turn_log]
            dest_log.write_text("\n".join(lines) + "\n", encoding="utf-8")

        # ----------------------------------------------------------
        # Copy experiment_data/ snapshot via f3dasm store()
        # ----------------------------------------------------------
        # store() writes to <project_dir>/experiment_data/; we want it
        # under out_dir/experiment_data/.
        # Temporarily point the data at out_dir so store() puts files
        # in the right place, then restore the original project_dir.
        original_project_dir = data._project_dir
        data.store(out_dir)
        # Restore the original project directory reference.
        data.set_project_dir(original_project_dir, in_place=True)

        # ----------------------------------------------------------
        # Write replicate.py
        # ----------------------------------------------------------
        self._write_replicate_script(out_dir)

        return out_dir

    # ------------------------------------------------------------------
    # _write_replicate_script (internal helper)
    # ------------------------------------------------------------------

    def _write_replicate_script(self, out_dir: Path) -> None:
        """Generate ``replicate.py`` inside ``out_dir``.

        The generated script loads the dataset from the deliverable
        folder, replays every ``strategy_calls`` triple from
        ``turn_log.jsonl`` in turn order using the registered strategy
        adapters (no LLM invoked), and prints the best design.

        Parameters
        ----------
        out_dir : Path
            Directory where ``replicate.py`` will be written.
        """

        # Collect (name, n_steps, params) triples in turn order.
        calls: list[tuple[str, int, dict]] = []
        for rec in self.turn_log:
            for call in rec.strategy_calls:
                calls.append((call[0], call[1], call[2]))

        # Serialise the calls list as a Python JSON-literal source block.
        calls_repr = json.dumps(calls, indent=4)

        objective_name = self.problem_schema.objective_name
        direction = self.problem_schema.objective_direction

        # Build the script using explicit string concatenation so that
        # the indentation of the generated file is unambiguous regardless
        # of the indentation level of this method in the source tree.
        # (textwrap.dedent fails when multi-line f-string interpolations
        # introduce lines with less leading whitespace than the template.)
        script = _build_replicate_script(
            calls_repr=calls_repr,
            objective_name=objective_name,
            direction=direction,
        )
        (out_dir / "replicate.py").write_text(script, encoding="utf-8")

    # ------------------------------------------------------------------
    # _render_context
    # ------------------------------------------------------------------

    def _render_context(self) -> str:
        """Render the AnalysisBase as a single prompt block.

        Concatenates ``objective_summary``, ``coverage_summary``, then
        ``last_5_rationales``.  If the total exceeds 8000 characters,
        truncates ``last_5_rationales`` from the oldest end.

        Returns
        -------
        str
            The rendered context string.
        """

        obj = self.analysis_base.get("objective_summary", "")
        cov = self.analysis_base.get("coverage_summary", "")
        rat = self.analysis_base.get("last_5_rationales", "")

        # Budget enforcement: truncate rationales from the oldest end.
        MAX_CHARS = 8000
        combined_without_rat = (
            f"## Objective Summary\n{obj}\n\n"
            f"## Coverage Summary\n{cov}\n\n"
        )
        remaining = MAX_CHARS - len(combined_without_rat)

        if remaining <= 0:
            logger.warning(
                "_render_context: context exceeds 8000 chars even without "
                "rationales; rationales section omitted."
            )
            rat_section = ""
        elif rat and len(rat) > remaining:
            logger.warning(
                "_render_context: last_5_rationales truncated to fit 8000 "
                "character budget."
            )
            rat_section = (
                "## Recent Rationales (oldest truncated)\n"
                + rat[-remaining:]
            )
        else:
            rat_section = (
                f"## Recent Rationales\n{rat}" if rat else ""
            )

        return combined_without_rat + rat_section

