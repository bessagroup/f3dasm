class ClaudeSDKAgent:
    """Concrete ``Agent`` wrapping an Anthropic Claude Agent SDK session.

    The session maintains conversation history across ``send()`` calls via
    the Claude Agent SDK's ``resume`` mechanism: each call stores the
    returned ``session_id`` and passes it as ``resume=`` on the next call
    so the subprocess picks up where it left off.

    Parameters
    ----------
    name : str
        Stable identifier (e.g. ``'strategizer'``).
    system_prompt : str
        The 4-field system prompt built by the topology.
    deny_list : list of str, optional
        Tool names to block at the SDK level (``disallowed_tools``).
    model : str, optional
        Claude model identifier; defaults to ``claude-haiku-4-5-20251001``.
    **sdk_kwargs : Any
        Extra keyword arguments forwarded to ``ClaudeAgentOptions``.

    Notes
    -----
    ``claude-agent-sdk`` is imported lazily so that f3dasm without the
    agentic extras can still import without raising ``ImportError``.

    Tools (Python callables) are wrapped into an in-process MCP server via
    ``create_sdk_mcp_server()`` on every ``send()`` call.  The MCP server
    name is derived from the agent name so it is stable across calls.

    The implementation is synchronous (``asyncio.run(...)`` bridges into
    the async SDK) because ``send()`` is called from synchronous
    orchestration code.
    """

    def __init__(
        self,
        name: str,
        system_prompt: str,
        deny_list: list[str] = (),
        model: str = "claude-haiku-4-5-20251001",
        **sdk_kwargs: Any,
    ) -> None:
        """Initialise the Claude Agent SDK wrapper.

        Parameters
        ----------
        name : str
            Stable identifier.
        system_prompt : str
            Agent system prompt forwarded to ``ClaudeAgentOptions``.
        deny_list : list of str, optional
            Tool names to block via ``disallowed_tools``.
        model : str, optional
            Claude model identifier.
        **sdk_kwargs : Any
            Extra kwargs forwarded to ``ClaudeAgentOptions``.
        """

        self.name = name
        self._system_prompt = system_prompt
        self._model = model
        self._deny_list = list(deny_list)
        self._sdk_kwargs = sdk_kwargs

        # Lazy import — raises ImportError only when a ClaudeSDKAgent is
        # actually constructed in an environment without the SDK installed.
        try:
            import claude_agent_sdk  # noqa: F401
            self._sdk_available = True
        except ImportError:
            self._sdk_available = False
            warnings.warn(
                "claude-agent-sdk package not found; "
                "ClaudeSDKAgent.send() will raise ImportError. "
                "Install it with: pip install 'f3dasm[agentic]' "
                "or use StubAgent for testing.",
                ImportWarning,
                stacklevel=2,
            )

        # Session ID returned by the last query(); used for resume= on the
        # next call so the CLI subprocess picks up the full conversation.
        self._session_id: Optional[str] = None

        # Conversation history maintained across send() calls.
        # Kept for diagnostics and potential fallback; primary persistence
        # is the CLI session referenced by self._session_id.
        self._history: list[dict[str, Any]] = []

    # ------------------------------------------------------------------
    # send
    # ------------------------------------------------------------------

    def send(self, message: str, tools: list) -> str:
        """Send a message to the Claude session and return its response.

        Conversation history is preserved across calls via the Claude Agent
        SDK's session-resume mechanism: the ``session_id`` returned by each
        ``query()`` run is stored in ``self._session_id`` and passed as
        ``resume=`` on the next call.

        Parameters
        ----------
        message : str
            The orchestrator's turn message sent to the agent.
        tools : list
            Per-turn tool callables to expose to the agent.  Each entry
            must be a Python callable whose ``__name__`` is a unique tool
            identifier and whose ``__doc__`` is used as the tool description.
            Parameter types are introspected from ``inspect.signature``; all
            parameters are mapped to ``str`` (JSON Schema ``"string"``) when
            the annotation is absent or cannot be resolved.

        Returns
        -------
        str
            Final assistant message text; concatenation of all
            ``TextBlock.text`` values from the last ``AssistantMessage``
            received before the ``ResultMessage``.

        Raises
        ------
        ImportError
            If ``claude-agent-sdk`` is not installed.
        RuntimeError
            If the SDK query returns an error result or emits no assistant
            text.
        """

        import asyncio
        import inspect

        # Raise early if the SDK is not available.
        if not self._sdk_available:
            raise ImportError(
                "claude-agent-sdk is required for ClaudeSDKAgent.send(). "
                "Install it with: pip install 'f3dasm[agentic]'"
            )

        from claude_agent_sdk import (
            AssistantMessage,
            ClaudeAgentOptions,
            ResultMessage,
            SdkMcpTool,
            TextBlock,
            create_sdk_mcp_server,
            query,
        )

        # ----------------------------------------------------------------
        # Build an in-process MCP server from the per-turn tool callables.
        # Each callable becomes one SdkMcpTool; the handler is a thin async
        # wrapper that calls the underlying synchronous Python function and
        # returns its result as MCP content text.
        # ----------------------------------------------------------------

        def _infer_schema(fn: Any) -> dict[str, Any]:
            """Derive a simple JSON Schema dict from ``fn``'s signature."""
            sig = inspect.signature(fn)
            props: dict[str, Any] = {}
            required: list[str] = []
            for pname, param in sig.parameters.items():
                ann = param.annotation
                if ann is inspect.Parameter.empty or ann is None:
                    # Fall back to string when annotation is absent.
                    json_type: dict[str, Any] = {"type": "string"}
                elif ann is int:
                    json_type = {"type": "integer"}
                elif ann is float:
                    json_type = {"type": "number"}
                elif ann is bool:
                    json_type = {"type": "boolean"}
                elif ann is dict:
                    json_type = {"type": "object"}
                elif ann is list:
                    json_type = {"type": "array"}
                else:
                    json_type = {"type": "string"}
                props[pname] = json_type
                if param.default is inspect.Parameter.empty:
                    required.append(pname)
            schema: dict[str, Any] = {
                "type": "object",
                "properties": props,
            }
            if required:
                schema["required"] = required
            return schema

        sdk_tools: list[SdkMcpTool] = []
        for fn in tools:
            # Capture fn in a closure so the async handler binds the
            # correct callable (not the last value of fn at loop end).
            def _make_handler(bound_fn: Any) -> Any:
                async def _handler(args: dict[str, Any]) -> dict[str, Any]:
                    try:
                        result = bound_fn(**args)
                    except Exception as exc:
                        return {
                            "content": [
                                {
                                    "type": "text",
                                    "text": (
                                        f"Tool {bound_fn.__name__} raised "
                                        f"{type(exc).__name__}: {exc}"
                                    ),
                                }
                            ],
                            "is_error": True,
                        }
                    return {
                        "content": [
                            {
                                "type": "text",
                                "text": (
                                    str(result)
                                    if result is not None
                                    else ""
                                ),
                            }
                        ]
                    }

                return _handler

            sdk_tools.append(
                SdkMcpTool(
                    name=fn.__name__,
                    description=(
                        (fn.__doc__ or fn.__name__).split("\n")[0].strip()
                    ),
                    input_schema=_infer_schema(fn),
                    handler=_make_handler(fn),
                )
            )

        # Stable server name derived from agent name; one server per agent.
        server_name = f"f3dasm_{self.name}_tools"

        # Build an in-process MCP server config (or an empty one if no tools
        # were provided on this turn).
        mcp_config = create_sdk_mcp_server(
            name=server_name,
            tools=sdk_tools if sdk_tools else None,
        )

        # ----------------------------------------------------------------
        # Build the tool allow-list from the registered tool names so the
        # SDK does not prompt the user to approve each tool invocation.
        # ----------------------------------------------------------------
        tool_names = [t.name for t in sdk_tools]

        # ----------------------------------------------------------------
        # Configure the SDK options.
        # ----------------------------------------------------------------
        options = ClaudeAgentOptions(
            system_prompt=self._system_prompt,
            model=self._model,
            mcp_servers={server_name: mcp_config},
            # Allow every tool registered in the per-turn MCP server without
            # interactive permission prompts.
            allowed_tools=tool_names,
            # Block any tool on the deny-list (e.g. Bash, Write).
            disallowed_tools=self._deny_list,
            # Resume the existing CLI session so conversation history is
            # preserved across send() calls.  On the first call (_session_id
            # is None) a fresh session is started.
            resume=self._session_id,
            # Only load user/project settings that are relevant; skip local
            # settings so the SDK runs in a predictable state.
        )

        # ----------------------------------------------------------------
        # Drive the async query() via asyncio.run().
        # Collect AssistantMessage blocks and the final ResultMessage.
        # ----------------------------------------------------------------
        assistant_texts: list[str] = []
        result_msg: Optional[ResultMessage] = None

        async def _run() -> None:
            nonlocal result_msg
            last_assistant: Optional[AssistantMessage] = None
            async for msg in query(prompt=message, options=options):
                if isinstance(msg, AssistantMessage):
                    last_assistant = msg
                elif isinstance(msg, ResultMessage):
                    result_msg = msg
                    # Extract text from the last AssistantMessage before
                    # the ResultMessage.
                    if last_assistant is not None:
                        for block in last_assistant.content:
                            if isinstance(block, TextBlock):
                                assistant_texts.append(block.text)
                    break

        asyncio.run(_run())

        # ----------------------------------------------------------------
        # Persist the session_id so the next send() can resume the session.
        # ----------------------------------------------------------------
        if result_msg is not None:
            self._session_id = result_msg.session_id

        # ----------------------------------------------------------------
        # Persist the turn in the local history list for diagnostics.
        # ----------------------------------------------------------------
        self._history.append({"role": "user", "content": message})
        combined = "".join(assistant_texts)
        if combined:
            self._history.append({"role": "assistant", "content": combined})

        # ----------------------------------------------------------------
        # Raise if the SDK reported an error result.
        # ----------------------------------------------------------------
        if result_msg is not None and result_msg.is_error:
            raise RuntimeError(
                f"ClaudeSDKAgent '{self.name}': SDK query returned an error. "
                f"Result: {result_msg}"
            )

        if not combined:
            logger.warning(
                "ClaudeSDKAgent '%s': query returned no assistant text. "
                "Returning empty string.",
                self.name,
            )

        return combined


