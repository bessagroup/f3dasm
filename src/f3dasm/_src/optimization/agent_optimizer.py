class StrategizerImplementerOptimizer(AgentOptimizer):
    """Level-1 two-agent topology: Strategizer proposes, Implementer executes.

    A single logical iteration produces two ``TurnRecord``s (one per agent)
    and possibly more if the Implementer calls ``ask_strategizer``.

    Parameters
    ----------
    strategizer_factory : callable
        Called with ``name=`` and ``system_prompt=`` (and any extras) to
        instantiate the Strategizer agent.
    implementer_factory : callable
        Called with ``name=`` and ``system_prompt=`` to instantiate the
        Implementer agent.
    physics_context : str
        Day-1 problem briefing.
    output_columns : dict[str, OutputSpec]
        Typed metadata for every output column.
    extra_strategies : list of StrategySpec, optional
        Additional strategy specs to merge into the registry.
    extra_adapters : dict[str, callable], optional
        Corresponding adapter callables for ``extra_strategies``.
    forbidden_extra : list of str, optional
        Extra forbidden parameter names.
    max_followups : int, default 2
        Maximum number of ``ask_strategizer`` round-trips per Implementer
        turn.
    objective_direction : {'minimize', 'maximize'}, default 'maximize'
        Direction of optimization.
    """

    def __init__(
        self,
        strategizer_factory: Callable[..., Agent],
        implementer_factory: Callable[..., Agent],
        physics_context: str,
        output_columns: dict[str, OutputSpec],
        extra_strategies: list[StrategySpec] = (),
        extra_adapters: Optional[dict[str, Callable]] = None,
        forbidden_extra: list[str] = (),
        max_followups: int = 2,
        objective_direction: ObjectiveDirection = "maximize",
    ) -> None:
        """Initialise the topology.

        Parameters
        ----------
        strategizer_factory : callable
            Factory for the Strategizer agent.
        implementer_factory : callable
            Factory for the Implementer agent.
        physics_context : str
            Day-1 problem briefing.
        output_columns : dict[str, OutputSpec]
            Typed metadata for every output column.
        extra_strategies : list of StrategySpec, optional
            Additional strategies to register.
        extra_adapters : dict[str, callable], optional
            Adapters corresponding to ``extra_strategies``.
        forbidden_extra : list of str, optional
            Extra forbidden parameter names.
        max_followups : int, default 2
            Follow-up budget per Implementer turn.
        objective_direction : {'minimize', 'maximize'}, default 'maximize'
            Direction of optimization.
        """

        super().__init__(
            physics_context=physics_context,
            output_columns=output_columns,
            forbidden_extra=forbidden_extra,
            objective_direction=objective_direction,
        )
        self._strategizer_factory = strategizer_factory
        self._implementer_factory = implementer_factory
        self._extra_strategies: list[StrategySpec] = list(extra_strategies)
        self._extra_adapters: dict[str, Callable] = (
            dict(extra_adapters) if extra_adapters else {}
        )
        self.max_followups = max_followups

        # Holds the data reference so narrate() can find it after call().
        self._last_data: Optional[ExperimentData] = None

        # Monotonic turn counter; managed by call().
        self._current_turn_id: int = 0

    # ------------------------------------------------------------------
    # arm
    # ------------------------------------------------------------------

    def arm(
        self,
        data: ExperimentData,
        data_generator: DataGenerator,
        input_name: str,
        output_name: str,
    ) -> None:
        """Arm the topology: build the registry and instantiate agents.

        Parameters
        ----------
        data : ExperimentData
            The shared dataset.
        data_generator : DataGenerator
            The evaluator.
        input_name : str
            Input parameter group name.
        output_name : str
            Objective output column name.
        """

        super().arm(data, data_generator, input_name, output_name)

        # Merge extra strategies into the registry.
        for spec in self._extra_strategies:
            self.strategy_specs[spec.name] = spec
        for name, adapter in self._extra_adapters.items():
            self.strategy_adapters[name] = adapter

        # Compose per-agent system prompts.
        strategizer_prompt = self._compose_strategizer_prompt(
            self.problem_schema
        )
        implementer_prompt = self._compose_implementer_prompt(
            self.problem_schema
        )

        # Instantiate the two persistent peer agents.
        self.agents = {
            "strategizer": self._strategizer_factory(
                name="strategizer",
                system_prompt=strategizer_prompt,
            ),
            "implementer": self._implementer_factory(
                name="implementer",
                system_prompt=implementer_prompt,
            ),
        }

    # ------------------------------------------------------------------
    # call
    # ------------------------------------------------------------------

    def call(
        self, data: ExperimentData, n_iterations: int, **kwargs: Any
    ) -> ExperimentData:
        """Run the Strategizer → Implementer loop for ``n_iterations``.

        Each logical iteration produces at least two ``TurnRecord``s
        (Strategizer + Implementer), plus additional Strategizer records
        for any ``ask_strategizer`` follow-ups.

        Parameters
        ----------
        data : ExperimentData
            The shared dataset.
        n_iterations : int
            Maximum number of logical iterations.
        **kwargs : Any
            Ignored; accepted for signature compatibility.

        Returns
        -------
        ExperimentData
            The updated (mutated-in-place) dataset.
        """

        self._last_data = data
        turn_id = self._current_turn_id

        for _iteration in range(n_iterations):
            # ---------------------------------------------------------
            # Strategizer turn
            # ---------------------------------------------------------
            ctx = self._compose_turn_message(turn_id)
            data = self._execute_turn(turn_id, "strategizer", ctx, data)
            turn_id += 1

            # Retrieve the emitted strategy from the previous record.
            strat_record = self.turn_log[-1]
            strategy = strat_record.emitted_strategy

            if strategy is None:
                logger.warning(
                    "Strategizer turn %d did not emit a strategy; "
                    "skipping Implementer turn.",
                    turn_id - 1,
                )
                continue

            # ---------------------------------------------------------
            # Implementer turn
            # ---------------------------------------------------------
            impl_msg = self._compose_implementer_message(
                turn_id, strategy
            )
            data = self._execute_turn(turn_id, "implementer", impl_msg, data)
            turn_id += 1

            # Store the current turn_id for follow-up routing.
            self._current_turn_id = turn_id

            if self._early_stop(data, self.turn_log):
                break

        self._current_turn_id = turn_id
        return data

    # ------------------------------------------------------------------
    # _followup_dispatch
    # ------------------------------------------------------------------

    def _followup_dispatch(self, question: str) -> str:
        """Route a clarification question to the Strategizer.

        Called by the ``ask_strategizer`` tool closure during an
        Implementer turn.  Each call increments the turn counter and
        produces a new Strategizer ``TurnRecord``.

        Parameters
        ----------
        question : str
            The Implementer's clarification request.

        Returns
        -------
        str
            The Strategizer's rationale (from the ``## Rationale`` section
            of its response).
        """

        if self._last_data is None:
            return "ERROR: no active data; followup dispatch not possible."

        turn_id = self._current_turn_id
        ctx = self._compose_turn_message(turn_id)
        followup_msg = (
            f"{ctx}\n\n---\nThe Implementer asks:\n{question}\n"
            "Please respond with your clarification under "
            "## Rationale."
        )

        self._execute_turn(
            turn_id, "strategizer", followup_msg, self._last_data
        )
        self._current_turn_id = turn_id + 1

        # Return the Strategizer's rationale as the response.
        if self.turn_log:
            return self.turn_log[-1].rationale or ""
        return ""

